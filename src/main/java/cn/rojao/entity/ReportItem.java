package cn.rojao.entity;

/**
 * 信息呈现上报列表实体
 * @author lenovo
 *
 */
public class ReportItem {
	//上报的信息素材key
	private String infoContentKey;
	//信息展示的时间点，采用UTC格式，比如2016-07-19T14:21:56Z
	private String displayTime;
	//信息是否被点击，0没有点击，1有点击
	private Long isClick;
	//用户在信息详情页上浏览的具体时间，进详情失败，该值传0
	private Long detailShowTime;
	//信息展示时长，单位秒
	private Long displayDuration;
	
	public String getInfoContentKey() {
		return infoContentKey;
	}
	public void setInfoContentKey(String infoContentKey) {
		this.infoContentKey = infoContentKey;
	}
	public String getDisplayTime() {
		return displayTime;
	}
	public void setDisplayTime(String displayTime) {
		this.displayTime = displayTime;
	}
	public Long getIsClick() {
		return isClick;
	}
	public void setIsClick(Long isClick) {
		this.isClick = isClick;
	}
	public Long getDetailShowTime() {
		return detailShowTime;
	}
	public void setDetailShowTime(Long detailShowTime) {
		this.detailShowTime = detailShowTime;
	}
	public Long getDisplayDuration() {
		return displayDuration;
	}
	public void setDisplayDuration(Long displayDuration) {
		this.displayDuration = displayDuration;
	}
	
	
}
